#include "libft.h"

int		ft_count_w(char const *s, char c)
{
	int i;

	i = 0;
	while (*s == c && *s)
		s++;
	while (*s)
	{
		while (*s != c && *s)
			s++;
		i++;
		while (*s == c && *s)
			s++;
	}
	return (i);
}
